#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	size_t			i;
	unsigned char	*a;

	i = 0;
	a = (unsigned char *) s;
	if (n == 0)
		return ;
	while (i < n)
	{
		a[i] = '\0';
		i++;
	}
}
